function setup() {
  createCanvas(600, 400);
  background(230);

  noStroke();

  // 얼굴을 암시하는 큰 타원
  fill(180, 120, 80);
  ellipse(300, 200, 220, 260);

  // 머리
  fill(40);
  arc(300, 150, 240, 180, PI, TWO_PI);

  // 눈을 추상화한 원
  fill(255);
  ellipse(250, 190, 50, 50);
  ellipse(350, 190, 50, 50);

  fill(0);
  ellipse(250, 190, 20, 20);
  ellipse(350, 190, 20, 20);

  // 코를 삼각형으로 표현
  fill(240, 180, 120);
  triangle(300, 200, 280, 240, 320, 240);

  // 입을 사각형으로 표현
  fill(220, 80, 80);
  rect(260, 270, 80, 20);

  // 장식 도형
  fill(60, 120, 220);
  rect(60, 60, 100, 100);

  fill(255, 200, 0);
  triangle(450, 80, 550, 80, 500, 160);

  fill(80, 180, 120);
  ellipse(500, 300, 100, 100);
}